    private void botaoSalvarAproveitamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarAproveitamentoActionPerformed
       String turma;
       String aluno = null;
       Double nota = null;
       Double frequencia = null;
        
//  Testa se os campos de nota e frequencia, os combos de aluno e turmas para ver se estão vazios   
        if(txtNota.getText().equals("") || txtFrequencia.getText().equals("") || comboAluno.getSelectedItem() == null || comboTurmaAluno.getSelectedItem() == null ){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatórios!");
            
//  Se nem um campo estiver vazio..
        }else{
            turma = comboTurmaAluno.getSelectedItem().toString();
            aluno = comboAluno.getSelectedItem().toString();            
            nota = Double.parseDouble(txtNota.getText());
            frequencia = Double.parseDouble(txtFrequencia.getText());
            
            ControladorAproveitamento CA = new ControladorAproveitamento();  
            
//  Envia os parametros para o controlador inserir no BD
            CA.IncluirAproveitamento(turma, aluno, nota, frequencia);
        }
        
//  Atualizar tabela
       listarAproveitamentos();
    }//GEN-LAST:event_botaoSalvarAproveitamentoActionPerformed

