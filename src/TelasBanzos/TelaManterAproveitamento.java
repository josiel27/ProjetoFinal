    private void listarComboTurmas() {
        conexao.conecta();//inciacao conexao com o BD
        String sql= "SELECT T.turma FROM tb_turma T\n" +
                    "INNER JOIN rl_turma_periodo TP\n" +
                    "ON T.cod_turma = TP.cod_turma\n" +
                    "INNER JOIN rl_aluno_turma A\n" +
                    "ON TP.cod_turma_periodo = A.cod_turma_periodo";
        conexao.SelectSQL(sql);
         try {
            while (conexao.rs.next()) {
                comboTurmaAluno.addItem(conexao.rs.getString("turma"));
            }
        } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error ao listar combo de turmas! ");
        }
        conexao.desconecta();//fecha a conexao  
    }

    private void listarAproveitamentos() {
        conexao.conecta();//inciacao conexao com o BD
        conexao.SelectSQL("SELECT A.matricula, A.nome, RL.nota, RL.frequencia, T.turma \n" +
"                            FROM rl_aluno_turma RL \n" +
"                            INNER JOIN tb_aluno A \n" +
"                            ON RL.matricula = A.matricula \n" +
"                            INNER JOIN rl_turma_periodo TP  \n" +
"                            ON TP.cod_turma_periodo = RL.cod_turma_periodo \n" +
"                            INNER JOIN tb_turma T  \n" +
"                            ON TP.cod_turma = T.cod_turma\n" +
"                            WHERE nota != 0 \n"+                
"                            ORDER BY matricula ASC" );
        tbAproveitamento.setModel(DbUtils.resultSetToTableModel(conexao.rs));
        conexao.desconecta();//fecha a conexao com o BD
    }

