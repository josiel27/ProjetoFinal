    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed
        int[] L = tbAproveitamento.getSelectedRows();
        conexao.conecta();//inicia conexao com o BD   
         
        if (L.length == 1) {
            String mat = tbAproveitamento.getValueAt(L[0], 0)+"";  
            String turma = tbAproveitamento.getValueAt(L[0], 4)+""; 
            int m = Integer.parseInt(mat);
            int cod_turma_periodo = 0;
            
//  SQL para descobrir o código da tabela rl_turma_periodo referente a turma         
            String sql ="SELECT TP.cod_turma_periodo FROM rl_turma_periodo TP\n" +
                        "INNER JOIN tb_turma T\n" +
                        "ON TP.cod_turma = T.cod_turma\n" +
                        "WHERE turma = '"+turma+"'";
            try {
                conexao.SelectSQL(sql);
                conexao.rs.next();
                cod_turma_periodo = conexao.rs.getInt("cod_turma_periodo");
            } catch (SQLException ex) {
                JOptionPane.showConfirmDialog(null, "Não foi possivel achar a turma!");
            }
            
//  DELETA da tabela rl_aluno_turma            
            sql = "DELETE FROM rl_aluno_turma WHERE matricula ="+m+" AND cod_turma_periodo ="+cod_turma_periodo;
            conexao.DeleteSQP(sql);  
            
            sql= "INSERT INTO rl_aluno_turma (matricula, cod_turma_periodo)\n" +
                    "VALUES("+m+","+cod_turma_periodo+")";
            conexao.ExecutaSQL(sql);            
        }
        
        conexao.desconecta();//fecha conexao com o BD
        limpaCampos();
        listarAproveitamentos();
    }//GEN-LAST:event_botaoExcluirActionPerformed

