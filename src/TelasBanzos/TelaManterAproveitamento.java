    private void comboAlunoMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_comboAlunoMouseEntered
       comboAluno.removeAllItems();
        conexao.conecta();
        int cod_turma = 0;
        String turma = comboTurmaAluno.getSelectedItem().toString();
        String sql = "SELECT cod_turma FROM tb_turma WHERE turma = '"+turma+"'";
        conexao.SelectSQL(sql);
        try {
            conexao.rs.next();
            cod_turma = Integer.parseInt(conexao.rs.getString("cod_turma"));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possivel achar o código da turma");
        }
        
        sql= "SELECT A.nome FROM tb_aluno A\n" +
                    "INNER JOIN rl_aluno_turma AT\n" +
                    "ON A.matricula = AT.matricula\n" +
                    "INNER JOIN rl_turma_periodo TP\n" +
                    "ON AT.cod_turma_periodo = TP.cod_turma_periodo\n" +
                    "INNER JOIN tb_turma T\n" +
                    "ON TP.cod_turma = T.cod_turma\n"+
                    "WHERE T.cod_turma ="+cod_turma;
        
        conexao.SelectSQL(sql);        
        try {
            while (conexao.rs.next()) {                 
                comboAluno.addItem(conexao.rs.getString("nome")); 
            }
        } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error ao listar turmas! ");
        }       
        conexao.desconecta(); 
    }//GEN-LAST:event_comboAlunoMouseEntered

    private void txtPesquisarAproveitamentosKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPesquisarAproveitamentosKeyTyped
        
//   PARA PESQUISAR OS CAMPOS DA TABELA     
        DefaultTableModel tabela_clientes = (DefaultTableModel) tbAproveitamento.getModel();
        final TableRowSorter<TableModel> sorter = new TableRowSorter<TableModel>(tabela_clientes);
        tbAproveitamento.setRowSorter(sorter);
        String text = txtPesquisarAproveitamentos.getText();
        if (text.length() == 0)
        {
            sorter.setRowFilter(null);
        }
        else
        {
            try
            {
                sorter.setRowFilter(
                    RowFilter.regexFilter(text));
            }
            catch (PatternSyntaxException pse)
            {
                System.err.println("Erro");
            }
        }
    }//GEN-LAST:event_txtPesquisarAproveitamentosKeyTyped

