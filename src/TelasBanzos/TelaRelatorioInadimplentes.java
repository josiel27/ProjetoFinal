    private void botaoGerarRelatorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoGerarRelatorioActionPerformed
        conexao.conecta();
        String sql= "SELECT A.matricula, A.nome, SP.situacao_pagamento, SP.qtd_mensalidade_pendente, SP.mes_pendente, SP.dias_atraso, SP.valor \n" +
                    "FROM tb_aluno A \n" +
                    "Inner Join tb_situacao_pagamento SP \n" +
                    "ON A.matricula = SP.matricula\n" +
                    "WHERE situacao_pagamento = 'Inadimplente'";

        try{
            conexao.SelectSQL(sql);
            JRResultSetDataSource relatResult = new JRResultSetDataSource(conexao.rs);
            JasperPrint jpPrint = JasperFillManager.fillReport("G:\\Projeto Final II\\PROTOTIPO FUNCIONAL\\ProjetoFinalII\\Relatorios\\AlunosInadimplentes.jasper", new HashMap(), relatResult);
            JasperViewer jv = new JasperViewer(jpPrint, false);
            jv.setVisible(true);//chama relat칩rio para visualiza칞칚o
            jv.setLocationRelativeTo(jv);//coloca a tela no meio
            jv.toFront();//seta o relatorio pra frente da aplica칞칚o
        }catch(JRException ex){
            JOptionPane.showMessageDialog(null, "N칚o foi possivel gerar o relat칩rio! " +ex.getMessage());
        }            
    }//GEN-LAST:event_botaoGerarRelatorioActionPerformed

